public class ColourPoint {
    private int iteration;
    private double zabs;



    public ColourPoint(int iteration,double zabs){
        this.iteration=iteration;
        this.zabs=zabs;
    }



    public int getIteration() {
        return iteration;
    }

    public double getZabs() {
        return zabs;
    }
}
